CREATE TABLE wimm_spending_list(
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    category TEXT NOT NULL,
    user_id INTEGER REFERENCES wimm_user(id) ON DELETE CASCADE NOT NULL
);

CREATE TABLE wimm_spending_item(
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    category_id INTEGER REFERENCES wimm_spending_list(id) ON DELETE CASCADE NOT NULL,
    item_name TEXT NOT NULL,
    spending DECIMAL(12,2) NOT NULL,
    content TEXT NOT NULL,
    date_created TIMESTAMPTZ NOT NULL DEFAULT NOW()
)